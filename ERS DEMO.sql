SELECT U.USER_FIRST_NAME, U.USER_LAST_NAME, R.REIMB_AMMOUNT, T.REIMB_TYPE, R.REIMB_SUBMITTED ,S.REIMB_STATUS
FROM ERS.REIMBURSEMENT AS R
INNER JOIN ERS.USERS AS U
ON R.REIMB_AUTHOR = U.ERS_USERS_ID
INNER JOIN ERS.REIMBURSEMENT_TYPE AS T
ON R.REIMB_TYPE_ID = T.REIMB_TYPE_ID
INNER JOIN ERS.REIMBURSEMENT_STATUS AS S
ON R.REIMB_STATUS_ID = S.REIMB_STATUS_ID;

SELECT R.REIMB_SUBMITTED, E.USER_FIRST_NAME, E.USER_LAST_NAME, 
T.REIMB_TYPE, R.REIMB_AMMOUNT, R.REIMB_DESCRIPTION ,S.REIMB_STATUS, 
R.REIMB_RESOLVED, M.USER_FIRST_NAME, M.USER_LAST_NAME 
FROM ERS.REIMBURSEMENT AS R
JOIN ERS.USERS AS E
ON R.REIMB_AUTHOR = E.ERS_USERS_ID
JOIN ERS.REIMBURSEMENT_TYPE AS T
ON R.REIMB_TYPE_ID = T.REIMB_TYPE_ID
JOIN ERS.REIMBURSEMENT_STATUS AS S
ON R.REIMB_STATUS_ID = S.REIMB_STATUS_ID
LEFT JOIN ERS.USERS AS M
ON R.REIMB_RESOLVER = M.ERS_USERS_ID
WHERE E.ERS_USERNAME = LOWER('LLEVIT');