package com.revature.model;

import java.awt.Image;
import java.time.LocalDateTime;

public class Reinbursement {
	private Integer ammount;
	private LocalDateTime submitted;
	private LocalDateTime resolved;
	private String description;
	private String author;
	private String resolver;
	private String status;
	private String type;
	
	public Reinbursement(Integer ammount, LocalDateTime submitted, LocalDateTime resolved, String description,
			String author, String resolver, String status, String type) {
		super();
		this.ammount = ammount;
		this.submitted = submitted;
		this.resolved = resolved;
		this.description = description;
		this.author = author;
		this.resolver = resolver;
		this.status = status;
		this.type = type;
	}

	public Reinbursement(Integer ammount, LocalDateTime submitted, String description, String author, String status,
			String type) {
		super();
		this.ammount = ammount;
		this.submitted = submitted;
		this.description = description;
		this.author = author;
		this.status = status;
		this.type = type;
	}

	public Integer getAmmount() {
		return ammount;
	}

	public void setAmmount(Integer ammount) {
		this.ammount = ammount;
	}

	public LocalDateTime getSubmitted() {
		return submitted;
	}

	public void setSubmitted(LocalDateTime submitted) {
		this.submitted = submitted;
	}

	public LocalDateTime getResolved() {
		return resolved;
	}

	public void setResolved(LocalDateTime resolved) {
		this.resolved = resolved;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getResolver() {
		return resolver;
	}

	public void setResolver(String resolver) {
		this.resolver = resolver;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}	
}

